package br.com.alura.threads.cap6;

/**
 * @author eltonf
 *
 */
public class Multiplicacao {

	private long valor1;
	private long valor2;

	/**
	 * @param valor1
	 * @param valor2
	 */
	public Multiplicacao(long valor1, long valor2) {
		this.valor1 = valor1;
		this.valor2 = valor2;
	}

	/**
	 * @return the valor1
	 */
	public long getValor1() {
		return valor1;
	}

	/**
	 * @return the valor2
	 */
	public long getValor2() {
		return valor2;
	}
}
